public class TwoDArray{
  public static void main(String[] args) {
    int [][]a=new int[4][];
    a[0]=new int[5];
    a[1]=new int[3];
    a[2]=new int[10];

    System.out.println("Length="+a[0].length);
    System.out.println("Length="+a[1].length);
    System.out.println("Length="+a[2].length);
  }
}

